  k.emit(s_nop())
  k.emit(v_readfirstlane_b32_e32(v[11], v[18]))
  k.waitcnt(lgkm=0)
  k.emit(s_mov_b32(s[85], 84148480))
  k.emit(s_mov_b32(s[86], 117834498))
